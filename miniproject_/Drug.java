    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        // TODO add your handling code here:
        try{
        String sql = "insert into DRUGS(DRUG_ID,DRUG_NAME,STORAGE_CONDITION,DRUG_CODE,Unit_price) values(?,?,?,?,?)";
        ps=con.prepareStatement(sql);
        ps.setString(1,Drug_id.getText());
        ps.setString(2,Drug_name.getText());
        ps.setInt(4,Integer.parseInt(Drug_code.getText()));
        ps.setString(3,Storage_condition.getText());
        ps.setString(5,Unit_price.getText());
        ps.executeQuery();
        JOptionPane.showMessageDialog(this,"Success");
        }
        catch(SQLException ex)
        {
             Logger.getLogger(MiniProject_.class.getName()).log(Level.SEVERE,null,ex);
             JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }//GEN-LAST:event_InsertActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE Drugs SET Drug_name = ?, Storage_condition = ?, Drug_code = ? ,Unit_price= ? WHERE Drug_id = ?";
            ps = con.prepareStatement(sql);
    
    // Set parameter
            ps.setString(1, Drug_name.getText());
            ps.setString(2, Storage_condition.getText());
            ps.setInt(3,Integer.parseInt(Drug_code.getText()));
            ps.setInt(4,Integer.parseInt(Unit_price.getText()));
            ps.setString(5, Drug_id.getText().trim());
            
    
    // Execute the update
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Updated");
            } else {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(MiniProject_.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid basic salary value. Please enter a valid number.");
        }
    }//GEN-LAST:event_UpdateActionPerformed

