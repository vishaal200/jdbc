    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
       
        // TODO add your handling code here:
        try{
        String sql="select * from Drugs where DRUG_ID= '"+ Drug_id.getText() + "'";
        st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
        rs=st.executeQuery(sql);
        
        if(rs.next()){
            Drug_name.setText(rs.getString(2));
            Drug_code.setText(rs.getString(4));
            Unit_price.setText(rs.getString(5));
            Storage_condition.setText(rs.getString(3));
        }
        else{
            JOptionPane.showMessageDialog(this, "No record found");
        }
       }
       catch(SQLException ex)
       {
            Logger.getLogger(MiniProject_.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(this,ex.getMessage());
       }
        
    }//GEN-LAST:event_SearchActionPerformed

