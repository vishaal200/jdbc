    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        try {
    String sql = "UPDATE Customer SET Cust_name = ?, Email = ?, Door_no = ?, City = ?, Pincode = ? WHERE Cust_id = ?";
    ps = con.prepareStatement(sql);

    // Set parameters
    ps.setString(1, Cust_name.getText());
    ps.setString(2, Email.getText());
    ps.setString(3, Door_no.getText());
    ps.setString(4, City.getText());
    ps.setString(5, Pincode.getText());
    ps.setString(6, Cust_id.getText().trim());

    // Execute the update
    int rowsAffected = ps.executeUpdate();
    if (rowsAffected > 0) {
        JOptionPane.showMessageDialog(this, "Successfully Updated");
    } else {
        JOptionPane.showMessageDialog(this, "Record Not Found");
    }
} catch (SQLException ex) {
    Logger.getLogger(MiniProject_.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, ex.getMessage());
} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, "Invalid Pincode value. Please enter a valid number.");
}

    }//GEN-LAST:event_UpdateActionPerformed

    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        // TODO add your handling code here:
try {
    String sql = "INSERT INTO Customer(Cust_id, Cust_name, Email, Door_no, City, Pincode) values (?, ?, ?, ?, ?, ?)";
    ps = con.prepareStatement(sql);
    ps.setString(1, Cust_id.getText());
    ps.setString(2, Cust_name.getText());
    ps.setString(3, Email.getText());
    ps.setInt(4, Integer.parseInt(Door_no.getText()));
    ps.setString(5, City.getText()); // Set the city correctlyy
    ps.setInt(6, Integer.parseInt(Pincode.getText())); // Set Pincode correctly as an integer

    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Success");
} 
catch (SQLException ex) {
    Logger.getLogger(MiniProject_.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, ex.getMessage());
}


    }//GEN-LAST:event_InsertActionPerformed

