    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        try {
    String sql = "select * from Customer where Cust_id= '" + Cust_id.getText() + "'";
    st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
    rs = st.executeQuery(sql);
    
    if (rs.next()) {
        Cust_name.setText(rs.getString(2));
        Email.setText(rs.getString(3));
        Door_no.setText(rs.getString(4));
        City.setText(rs.getString(5));
        Pincode.setText(rs.getString(6));
    } else {
        JOptionPane.showMessageDialog(this, "No record found");
    }
} catch (SQLException ex) {
    Logger.getLogger(MiniProject_.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, ex.getMessage());
}

    }//GEN-LAST:event_SearchActionPerformed

